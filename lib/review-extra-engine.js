'use strict';

// investigate engine: structured root-cause investigation template.
// no fixes proposed inline - the point is to think before patching.
//
// second-opinion engine: builds a clean packet to paste into another tool.
// no shelling out. model-agnostic.

const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

function slugify(s, max = 40) {
  return String(s).toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .slice(0, max) || 'untitled';
}
function dateStamp() {
  const d = new Date();
  return `${d.getUTCFullYear()}${String(d.getUTCMonth() + 1).padStart(2, '0')}${String(d.getUTCDate()).padStart(2, '0')}`;
}

function tryExec(cmd, root) {
  try { return execSync(cmd, { cwd: root, encoding: 'utf8', stdio: ['ignore', 'pipe', 'ignore'], timeout: 3000 }); }
  catch (_e) { return null; }
}

function generateInvestigate({ symptom }) {
  const slug = slugify(symptom);
  const stamp = dateStamp();
  return {
    relPath: `.dragoon/investigations/investigate-${slug}-${stamp}.md`,
    content: `# investigate: ${symptom}

> generated by dragoon ${stamp}. fill in. the rule: no patches until "root cause" is filled.

## symptom

${symptom}

## reproduction steps

1. _exact steps a stranger could follow_
2. _what happens_
3. _what you expected_

## first observed

- **when**: _date / commit sha_
- **where**: _environment, browser, os_
- **how often**: _every time / sometimes / once_

## what changed recently

_run \`git log --oneline -20\`. paste suspect commits._

## hypotheses (and how to test each)

| # | hypothesis | how to test | result |
|---|-----------|-------------|--------|
| 1 | _what you think might be wrong_ | _command, log, or check that proves or disproves it_ | _filled after testing_ |
| 2 | | | |
| 3 | | | |

## evidence collected

_logs, error messages, stack traces. paste verbatim, then label._

\`\`\`
paste evidence here
\`\`\`

## root cause

_only fill in once you can reproduce, predict, and explain. if you can't do all three, you have not found the root cause._

## fix

- [ ] _the change_
- [ ] _the regression test that proves the fix_
- [ ] _docs/comments updated_
- [ ] _related code paths reviewed for the same bug elsewhere_

## prevention

_how do we make sure this class of bug doesn't recur? a lint rule, a type, a test, a refactor?_
`
  };
}

function generateSecondOpinion({ topic, root, includeFiles = [] }) {
  const slug = slugify(topic);
  const stamp = dateStamp();

  // gather context: git head, branch, recent log, optional file contents
  const branch = tryExec('git rev-parse --abbrev-ref HEAD', root);
  const head = tryExec('git rev-parse --short HEAD', root);
  const log = tryExec('git log --oneline -10', root);

  // optional included files (max 5, max 200 lines each, no path traversal)
  const fileBlocks = [];
  for (const f of includeFiles.slice(0, 5)) {
    const safeRel = path.normalize(f).replace(/^\/+/, '');
    if (safeRel.includes('..')) continue;
    const abs = path.join(root, safeRel);
    if (!abs.startsWith(path.resolve(root))) continue;
    if (!fs.existsSync(abs)) {
      fileBlocks.push({ rel: safeRel, error: 'not found' });
      continue;
    }
    let content;
    try { content = fs.readFileSync(abs, 'utf8'); } catch (_e) { continue; }
    const truncated = content.split('\n').slice(0, 200).join('\n');
    fileBlocks.push({ rel: safeRel, content: truncated, truncated: content.split('\n').length > 200 });
  }

  const filesSection = fileBlocks.length > 0
    ? fileBlocks.map(f => f.error
        ? `### \`${f.rel}\`\n\n_${f.error}_\n`
        : `### \`${f.rel}\`${f.truncated ? ' (truncated to first 200 lines)' : ''}\n\n\`\`\`\n${f.content}\n\`\`\`\n`
      ).join('\n')
    : '_no files attached. pass --file <path> to include specific files._';

  return {
    relPath: `.dragoon/second-opinion/${slug}-${stamp}.md`,
    content: `# second opinion request: ${topic}

> generated by dragoon ${stamp}. paste this entire file into another model or share with a colleague.
> dragoon does NOT call other models for you. this is your packet.

## the question

${topic}

## what I've already tried

_list approaches you've considered or tried, and why they didn't fit._

1.
2.
3.

## constraints

_anything that rules out otherwise-obvious answers (legacy, deadlines, deps, taste)._

-
-

## context

- branch: \`${branch ? branch.trim() : 'unknown'}\`
- head: \`${head ? head.trim() : 'unknown'}\`

### recent commits

\`\`\`
${log ? log.trim() : 'not a git repo or no history'}
\`\`\`

## relevant files

${filesSection}

## what would help most

- [ ] a different approach I haven't considered
- [ ] confirmation my approach is reasonable
- [ ] specific implementation review
- [ ] tradeoff analysis between two paths
- [ ] something else: _..._
`
  };
}

module.exports = { generateInvestigate, generateSecondOpinion };
